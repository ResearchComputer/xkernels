"""Minimal watchdog-free RCCL/NCCL communicator (ctypes over librccl.so).

Why this exists (issue #12 graph-capture deep-dive): HIP-graph capturing a
`torch.distributed` collective on the beverin stack fails because the
ProcessGroupNCCL *watchdog* thread polls `hipEventQuery` on the capturing stream
("operation not permitted when stream is capturing"). That guard is not
env-tunable here. Serve frameworks (vLLM/SGLang, tokenspeed-amd) sidestep it by
driving captured collectives through a raw NCCL communicator with no watchdog.
This is that, in ~150 lines.

The unique-id handshake rides the existing torch.distributed *store* (a TCPStore
created by init_process_group), so we never issue a collective through the
watchdog'd PG — only eager barriers/broadcasts, which never run during capture.

Datatype/op enums (NCCL ABI): bf16=9, fp32=7; sum=0.
"""
from __future__ import annotations

import ctypes
import os

import torch
import torch.distributed as dist

# --- NCCL ABI constants ---
NCCL_FLOAT32 = 7
NCCL_BFLOAT16 = 9
NCCL_SUM = 0

_TORCH_DT_TO_NCCL = {torch.float32: NCCL_FLOAT32, torch.bfloat16: NCCL_BFLOAT16}


class NcclUniqueId(ctypes.Structure):
    _fields_ = [("internal", ctypes.c_byte * 128)]


def _load_librccl() -> ctypes.CDLL:
    # Prefer the one torch is linked against (same ABI as the build).
    cand = os.path.join(os.path.dirname(torch.__file__), "lib", "librccl.so")
    for path in (cand, "librccl.so", "libnccl.so.2", "libnccl.so"):
        try:
            return ctypes.CDLL(path)
        except OSError:
            continue
    raise OSError("could not load librccl.so / libnccl.so")


_LIB = _load_librccl()


def _decl(name, restype, argtypes):
    fn = getattr(_LIB, name)
    fn.restype = restype
    fn.argtypes = argtypes
    return fn


c_vp = ctypes.c_void_p
_ncclGetUniqueId = _decl("ncclGetUniqueId", ctypes.c_int, [ctypes.POINTER(NcclUniqueId)])
_ncclCommInitRank = _decl(
    "ncclCommInitRank", ctypes.c_int,
    [ctypes.POINTER(c_vp), ctypes.c_int, NcclUniqueId, ctypes.c_int],
)
_ncclCommDestroy = _decl("ncclCommDestroy", ctypes.c_int, [c_vp])
_ncclAllReduce = _decl(
    "ncclAllReduce", ctypes.c_int,
    [c_vp, c_vp, ctypes.c_size_t, ctypes.c_int, ctypes.c_int, c_vp, c_vp],
)
_ncclReduceScatter = _decl(
    "ncclReduceScatter", ctypes.c_int,
    [c_vp, c_vp, ctypes.c_size_t, ctypes.c_int, ctypes.c_int, c_vp, c_vp],
)
_ncclAllGather = _decl(
    "ncclAllGather", ctypes.c_int,
    [c_vp, c_vp, ctypes.c_size_t, ctypes.c_int, c_vp, c_vp],
)


def _check(rc: int, what: str):
    if rc != 0:
        raise RuntimeError(f"{what} failed: ncclResult={rc}")


def _stream_ptr() -> int:
    # Read at call time so this resolves to the graph capture stream inside capture.
    return torch.cuda.current_stream().cuda_stream


class NcclComm:
    """A raw NCCL communicator over a subset of ranks. No watchdog -> capturable.

    ``ranks`` is the list of *global* ranks in this comm (sorted). The unique id
    is generated by ``ranks[0]`` and shared via the torch store under ``key``.
    """

    def __init__(self, ranks: list[int], global_rank: int, key: str):
        self.ranks = list(ranks)
        self.nranks = len(self.ranks)
        self.rank_in_comm = self.ranks.index(global_rank)
        store = dist.distributed_c10d._get_default_store()
        uid = NcclUniqueId()
        if self.rank_in_comm == 0:
            _check(_ncclGetUniqueId(ctypes.byref(uid)), "ncclGetUniqueId")
            # hex-encode: the 128-byte id has arbitrary bytes (incl. NUL); the
            # store round-trips str/bytes most safely as hex.
            store.set(key, bytes(uid.internal).hex())
        else:
            raw = store.get(key)  # blocks until root sets it; returns bytes
            hexs = raw.decode() if isinstance(raw, (bytes, bytearray)) else raw
            ctypes.memmove(uid.internal, bytes.fromhex(hexs), 128)
        self._comm = c_vp()
        _check(
            _ncclCommInitRank(ctypes.byref(self._comm), self.nranks, uid, self.rank_in_comm),
            f"ncclCommInitRank({key})",
        )

    def all_reduce(self, t: torch.Tensor):
        dt = _TORCH_DT_TO_NCCL[t.dtype]
        _check(
            _ncclAllReduce(t.data_ptr(), t.data_ptr(), t.numel(), dt, NCCL_SUM,
                           self._comm, _stream_ptr()),
            "ncclAllReduce",
        )

    def reduce_scatter(self, out: torch.Tensor, inp: torch.Tensor):
        dt = _TORCH_DT_TO_NCCL[inp.dtype]
        _check(
            _ncclReduceScatter(inp.data_ptr(), out.data_ptr(), out.numel(), dt, NCCL_SUM,
                               self._comm, _stream_ptr()),
            "ncclReduceScatter",
        )

    def all_gather(self, out: torch.Tensor, inp: torch.Tensor):
        dt = _TORCH_DT_TO_NCCL[inp.dtype]
        _check(
            _ncclAllGather(inp.data_ptr(), out.data_ptr(), inp.numel(), dt,
                          self._comm, _stream_ptr()),
            "ncclAllGather",
        )

    def destroy(self):
        if self._comm:
            _ncclCommDestroy(self._comm)
            self._comm = c_vp()
